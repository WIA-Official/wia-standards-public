## 상세 기술 분석

### 첨단 패키징 재료

반도체 패키징에 사용되는 재료는 전기적, 열적, 기계적 특성이 모두 우수해야 합니다. 주요 재료는 다음과 같습니다:

**기판 재료**:
- FR-4: 전통적인 에폭시-유리섬유 복합재료로 저비용이지만 고주파 특성이 제한적
- BT 레진: 비스말레이미드-트리아진 수지로 우수한 전기적 특성과 낮은 CTE 제공
- ABF: Ajinomoto Build-up Film으로 미세 라인/스페이스 구현 가능
- 유리 기판: 매우 낮은 유전 손실과 우수한 평탄도를 제공하여 차세대 패키징에 적용

**몰딩 컴파운드**:
- 에폭시 수지를 기반으로 실리카 필러를 70-90% 함유
- 낮은 CTE (10-15 ppm/°C)로 실리콘과의 열팽창 차이 최소화
- 높은 열전도도 버전은 알루미나, 질화붕소 등의 고열전도성 필러 사용
- 할로겐 프리 제형으로 환경 규제 대응

**솔더 재료**:
- SAC305 (Sn-3.0Ag-0.5Cu): 가장 널리 사용되는 무연 솔더 합금
- SAC405 (Sn-4.0Ag-0.5Cu): 더 높은 은 함량으로 기계적 강도 향상
- SnBi: 저온 솔더링이 필요한 응용에 사용
- 마이크로 범프용 고온 솔더: SAC 합금 또는 구리 필러 범프

**언더필 재료**:
- 에폭시 기반 열경화성 수지
- 낮은 CTE와 높은 유리 전이 온도(Tg)
- 모세관 언더필: 리플로우 후 모세관 작용으로 주입
- 노플로우 언더필: 리플로우 전 디스펜스, 리플로우 중 경화
- 몰디드 언더필: 몰딩과 언더필을 단일 공정으로 통합

**열 인터페이스 재료 (TIM)**:
- 열전도 그리스/페이스트: 1-5 W/mK, 도포 용이
- 상변화 재료: 실온에서 고체, 작동 시 연화
- 열 패드: 사전 성형, 취급 편리
- 솔더 TIM: 20-80 W/mK로 최고 성능, 재작업 어려움
- 액체 금속 TIM: 갈륨 합금 기반, 70+ W/mK

### 제조 공정 상세

**TSV 제조 공정**:
1. 실리콘 웨이퍼 준비 및 패터닝
2. DRIE(Deep Reactive Ion Etching)로 고종횡비 홀 에칭
3. 열산화 또는 유전체 증착으로 전기적 절연
4. 배리어/시드 레이어 증착 (Ti/Cu 또는 Ta/Cu)
5. 구리 전기도금으로 비아 충진
6. CMP로 과잉 구리 제거 및 평탄화
7. 웨이퍼 후면 공정 (박형화, TSV 노출, UBM 형성)

**RDL (Redistribution Layer) 형성**:
1. 유전체 층 증착 (폴리이미드 또는 PBO)
2. 포토리소그래피로 비아 개구부 정의
3. 유전체 에칭으로 패드 노출
4. 시드 레이어 스퍼터링
5. 포토레지스트 패터닝
6. 구리 전기도금 (5-10 μm 두께)
7. 포토레지스트 제거 및 시드 레이어 에칭
8. 보호층 증착 및 패터닝 (필요시 다층 RDL 반복)

**다이 본딩 및 조립**:
1. 웨이퍼 다이싱으로 개별 다이 분리
2. 다이 검사 및 선별 (KGD 테스트)
3. Pick-and-Place로 정밀 배치 (±10 μm 정렬 정확도)
4. 플럭스 도포
5. 리플로우 (230-260°C 피크 온도)
6. 언더필 디스펜스
7. 언더필 경화 (150°C, 30-60분)
8. 플럭스 클리닝 (필요시)

**패키지 몰딩**:
1. 몰드 프레임에 기판 로딩
2. 몰딩 컴파운드 트랜스퍼 몰딩
3. 경화 (175°C, 2-4시간)
4. 디게이팅 및 디플래싱
5. 마킹 (레이저 또는 잉크젯)
6. 특성 측정 및 검사

