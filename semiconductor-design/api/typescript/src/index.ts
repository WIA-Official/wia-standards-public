/**
 * Create a signal
 */
export function createSignal(
  name: string,
  direction: Direction,
  width: number = 1
): Signal {
  return {
    name,
    direction,
    width,
  };
}

/**
 * Create a port
 */
export function createPort(
  name: string,
  direction: Direction,
  width: number = 1,
  options?: Partial<Port>
): Port {
  return {
    name,
    direction,
    width,
    ...options,
  };
}

/**
 * Create a parameter
 */
export function createParameter(
  name: string,
  defaultValue: number | string,
  description?: string
): Parameter {
  return {
    name,
    type: typeof defaultValue === 'number' ? 'integer' : 'string',
    default_value: defaultValue,
    description,
  };
}

/**
 * Create a module
 */
export function createModule(
  name: string,
  ports: Port[],
  language: HDL = 'verilog',
  parameters: Parameter[] = []
): Module {
  return {
    name,
    ports,
    parameters,
    language,
  };
}

